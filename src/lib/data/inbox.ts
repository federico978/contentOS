/** Convert "DD.MM.YYYY" → "YYYY-MM-DD" for chronological sorting */
function iso(fecha: string): string {
  const [d, m, y] = fecha.split('.')
  return `${y}-${m.padStart(2, '0')}-${d.padStart(2, '0')}`
}

